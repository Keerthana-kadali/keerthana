package basicsofjava;

public class RelationalOperator {

	public static void main(String[] args) {
    int a=10 ,b=15;
    
       //equal to
		System.out.println("a==b "+ (a==b)); //false
		
		//Not equal to
		System.out.println("a!=b " + (a!=b));  //true
		
		 //Greater than
		System.out.println("a>b " + (a>b));  //false
		
		//less than
		System.out.println("a<b " + (a<b));  //true
		
		//greater than or equal to
		System.out.println("a>=b " + (a>=b)); //false
		
		//less than or equal to
		System.out.println("a<=b " + (a<=b)); //true

	}

}

	


