package basicsofjava;

public class TypecastingDemo {

	public static void main(String[] args) {
		
		//implicit casting
		
		byte num=123;
		int i=num;
		System.out.println(i);
		float f1=55.14f;
		double d1=f1;
		System.out.println(d1);
		
		// explicit casting
		
		double d2=11.52d;
		long l=(long)d2;
		System.out.println(1);
		float f2=66.56f;
		int li=(int)f2;
		System.out.println(li);
		
		
	}
	}