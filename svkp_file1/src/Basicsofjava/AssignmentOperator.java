package Basicsofjava;

public class AssignmentOperator {

	public static void main(String[] args) {
	int a=20;
	//Add and Assign
	a+=5;
	System.out.println("a after +=" +a);
	//Sub and Assign
	a-=10;
	System.out.println("a after -=" +a);
	//Multiply and Assign
	a*=4;
	System.out.println("a after *=" +a);
	//Divide and Assign
	a/=3;
	System.out.println("a after /=" +a);
	System.out.println(a);
	//Modulus and Assign
	a%=2;
	System.out.println("a after %=" +a);

	}

}
