package org.tnsif.capgemini.c2tc.wrapperclass;

public class AutoBoxing_UnBoxing {

	public static void main(String[] args) {
		//Autoboxing:converting primitive data type into wrapperclass
		byte primitivebyte =1;
		byte wrapperbyte= primitivebyte;
		System.out.println("Autoboxing "+wrapperbyte);
		//unboxing:converting wrapperclass into primitive data type
		byte unboxingbyte= wrapperbyte;
		System.out.println("unboxing "+unboxingbyte);
		
		//Autoboxing:converting primitive data type into wrapperclass
		int primitiveint=2;
		int wrapperint=primitiveint; 
		System.out.println("Autoboxing "+wrapperint);
		//unboxing:converting wrapperclass into primitive data type
		int unboxingint=wrapperint;
		System.out.println("unboxing "+ unboxingint);
		
		//Autoboxing:converting primitive data type into wrapperclass
		short primitiveshort=3;
		short wrappershort=primitiveshort;
		System.out.println("Autoboxing "+ wrappershort );
		//unboxing:converting wrapperclass into primitive data type
		short unboxingshort=wrappershort;
		System.out.println("unboxing "+ unboxingshort );
		
		//Autoboxing:converting primitive data type into wrapperclass
		long primitivelong=4L;
		long wrapperlong=primitivelong;
		System.out.println("Autoboxing "+ wrapperlong );
		//unboxing:converting wrapperclass into primitive data type
		long unbxinglong=wrapperlong;
		System.out.println("unboxing "+ unbxinglong);
		
		//Autoboxing:converting primitive data type into wrapperclass
		float primitivefloat=5f;
		float wrapperfloat=primitivefloat;
		System.out.println("Autoboxing"+  wrapperfloat);
		//unboxing:converting wrapperclass into primitive data type
		float unboxingfloat=wrapperfloat;
		System.out.println("unboxing "+ unboxingfloat );
		
		//Autoboxing:converting primitive data type into wrapperclass
		double primitivedouble=6d;
		double wrapperdouble= primitivedouble;
		System.out.println("Autoboxing "+ wrapperdouble);
		//unboxing:converting wrapperclass into primitive data type
		double unboxingdouble=wrapperdouble;
		System.out.println("unboxing "+ unboxingdouble);
		
		//Autoboxing:converting primitive data type into wrapperclass
		boolean primitiveboolean=true;
		boolean wrapperboolean=primitiveboolean;
		System.out.println("Autoboxing "+ wrapperboolean);
		//unboxing:converting wrapperclass into primitive data type
		boolean unboxingboolean=wrapperboolean;
		System.out.println("unboxing "+ unboxingboolean);
		
		}

	}


	
